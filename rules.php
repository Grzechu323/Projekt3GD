            <h4>ZASADY GRY</h4>
            <br>
            <p>Grę zawsze rozpoczynają białe, co daje im pewną inicjatywę w początkowej fazie partii.
                Gracze na zmianę wykonują posunięcia swoimi bierkami zgodnie z zasadami ruchu dla danej bierki i,
                jeśli wejdzie ona na pole zajmowane przez przeciwnika, zbija jego bierkę.<br>
                Szach jest groźbą zbicia króla, która musi być zażegnana przez przeciwnika w następnym posunięciu
                (króla nie można zbić). Mat, czyli postawienie króla przeciwnika w szachu, przed którym nie ma obrony,
                kończy partię i oznacza zwycięstwo gracza, którego bierka matuje króla przeciwnika. Remis występuje w przypadku, gdy:</p>
            <ul>
                <li>gracze uzgodnili taki wynik partii,</li>
                <li>na planszy wystąpił pat (jedna ze stron nie może wykonać prawidłowych posunięć, a jej król nie jest szachowany),</li>
                <li>żadna ze stron nie posiada środków niezbędnych do wygranej (choćby teoretycznej),</li>
                <li>powstała tzw. martwa pozycja, czyli żaden z graczy nie jest w stanie dać mata nawet przy najlepszej grze.</li>
            </ul>
            <p>Partia może zakończyć się remisem (na wniosek jednej ze stron), gdy:</p>
            <ul>
                <li>identyczna pozycja na planszy pojawiła się trzykrotnie,</li>
                <li>jedna ze stron daje wiecznego szacha,</li>
                <li>wykonano pięćdziesiąt posunięć bez ruchu pionem i bicia dowolną bierką.</li>
            </ul>
